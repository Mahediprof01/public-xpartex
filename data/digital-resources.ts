// Helper function to get digital resource by ID
export const getDigitalResourceById = (id: string): DigitalResource | undefined => {
  return digitalResourcesData.find(resource => resource.id === id)
}

// Helper function to get digital resources by category
export const getDigitalResourcesByCategory = (category: string): DigitalResource[] => {
  if (category === "all") return digitalResourcesData
  return digitalResourcesData.filter(resource => resource.category.toLowerCase() === category.toLowerCase())
}
